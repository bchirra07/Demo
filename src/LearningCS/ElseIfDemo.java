package LearningCS;

public class ElseIfDemo {

	public static void main(String[] args) {
		int a=11;
		
		if(a==12)
		{
			System.out.println("The Value of variable 'a' is 12");
			
		}else if(a==11)
		{
			System.out.println("The Value of variable 'a' is 11");
		}else if(a==13)
		{
			System.out.println("The Value of variable 'a' is 13");
		}

	}

}
